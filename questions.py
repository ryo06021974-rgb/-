def validate_questions():
    role_counts = Counter(
        role
        for question in QUESTIONS
        for role in question["choices"].values()
    )

    if len(QUESTIONS) != 24:
        raise ValueError(f"質問数は24問にしてください。現在は{len(QUESTIONS)}問です。")

    for index, question in enumerate(QUESTIONS, start=1):
        if len(question["choices"]) != 4:
            raise ValueError(f"Q{index} が4択になっていません。")

    unknown_roles = set(role_counts) - set(ROLES)
    if unknown_roles:
        raise ValueError(f"ROLESに存在しない役職があります: {sorted(unknown_roles)}")

    missing_roles = set(ROLES) - set(role_counts)
    if missing_roles:
        raise ValueError(f"QUESTIONSに出てこない役職があります: {sorted(missing_roles)}")

    for role in ROLES:
        if role_counts[role] != 12:
            raise ValueError(f"{role} は{role_counts[role]}回です。12回にしてください。")


validate_questions()

